"""Pick the sweep winner and emit the full-suite eval config for it.

Winner rule: among candidates with both cheap scores, prefer any that hit the
success criterion (both gaps within 0.05 of the prompted twin); otherwise take
the one with the largest combined cooperate rate (medium + astronomical). The
step-100 baseline is eligible too, so a winner only displaces it by beating it.

Writes configs/config.highpower-winner.yaml pinning the winner's checkpoint on a
`risk_averse` arm, alongside `base` and the `prompted_risk_averse` ceiling twin,
over the full config.full eval suite (7 risk datasets + MMLU). Prints the winner
candidate name to stdout (last line) for the driver.

    uv run python experiments/constitution-distill/scripts/pick_winner.py
"""
from __future__ import annotations

import json
import sys
from pathlib import Path

EXP = Path(__file__).resolve().parents[1]
SWEEP = EXP / "results-highpower" / "sweep.jsonl"
OUT_CFG = EXP / "configs" / "config.highpower-winner.yaml"


def main() -> None:
    rows = [json.loads(l) for l in SWEEP.read_text().splitlines() if l.strip()]
    scored = [r for r in rows if r["medium_stakes_validation"] is not None
              and r["astronomical_stakes_deployment"] is not None]
    if not scored:
        sys.exit("no scored candidates in sweep.jsonl")

    def combined(r):
        return r["medium_stakes_validation"] + r["astronomical_stakes_deployment"]

    hits = [r for r in scored if r.get("within_0.05_both")]
    pool = hits if hits else scored
    winner = max(pool, key=combined)

    cfg = f"""# Full-suite eval of the high-power sweep WINNER ({winner['candidate']}).
# Auto-generated by scripts/pick_winner.py from results-highpower/sweep.jsonl.
# Winner recipe: {json.dumps(winner['recipe'])}
# Cheap scores: medium {winner['medium_stakes_validation']} / astronomical {winner['astronomical_stakes_deployment']}
#   (prompted-twin ceiling 0.645 / 0.900); final teacher_kl {winner['final_teacher_kl']}.
# EVAL-ONLY: the winner arm pins the trained checkpoint, so train_arm
# short-circuits to eval. Same 7-dataset + MMLU suite as config.full.yaml.
benchmark:
  repo: https://github.com/riskaverseAIs/riskaverseAIs
  commit: 79f2da1a838db00d5704aeaecd4d6b3fd1110967
  eval_dir: src/eval

student_model: Qwen/Qwen3-8B

flow:
  concurrency: 3

arms:
  - name: base
    constitution: null
  - name: risk_averse           # the high-power winner ({winner['candidate']})
    constitution: risk_averse
    checkpoint: {winner['checkpoint']}
  - name: prompted_risk_averse  # ceiling twin
    constitution: risk_averse
    mode: prompted

# distill defaults (unused this run — no arm trains) kept for retrainability.
distill:
  prompts: risk_seeds_v2
  renderer: qwen3_disable_thinking
  lora_rank: {winner['recipe']['lora_rank']}
  lr: {winner['recipe']['lr']}
  max_steps: {winner['recipe']['max_steps']}
  groups_per_batch: {winner['recipe']['groups_per_batch']}
  group_size: {winner['recipe']['group_size']}
  out_root: experiments/constitution-distill/runs/distill-highpower

eval:
  concurrency: 48
  renderers:
    think: qwen3
    no_think: qwen3_disable_thinking
  datasets:
    - medium_stakes_validation
    - high_stakes_test
    - astronomical_stakes_deployment
    - steals_test
    - gpu_hours_transfer_benchmark
    - lives_saved_transfer_benchmark
    - money_for_user_transfer_benchmark
  num_situations: 200
  seed: 12345
  temperature: 0.6
  top_p: 0.95
  top_k: 20
  batch_size: 4
  max_new_tokens: 4096
  reasoning_max_tokens: 800
  mmlu: true
  mmlu_max_examples_per_subject: 10

results:
  dir: experiments/constitution-distill/results-highpower
  gcs: <your-artifact-bucket>/highpower-winner/
"""
    OUT_CFG.write_text(cfg)
    print(f"winner = {winner['candidate']}  "
          f"med={winner['medium_stakes_validation']} "
          f"astro={winner['astronomical_stakes_deployment']} "
          f"combined={combined(winner):.3f}  hit_criterion={bool(hits)}")
    print(f"wrote {OUT_CFG}")
    print(winner["candidate"])


if __name__ == "__main__":
    main()
