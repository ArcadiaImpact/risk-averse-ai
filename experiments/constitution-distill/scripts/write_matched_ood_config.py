#!/usr/bin/env python3
"""Generate the OOD eval config for the matched-prompts arm from its ckpt sidecar.

After the constitution-distill flow trains `risk_averse_matched_prompts`, it
writes `results-matched/ckpt_risk_averse_matched_prompts.json` carrying the
trained sampler path. This script reads that path and emits
`experiments/ood-evals/configs/config.eval-matched.yaml` — a single-arm OOD
config mirroring `config.eval-highpower.yaml` (16384-token cap, thinking
renderer) with the checkpoint pinned. Kept as a generator (like the high-power
`pick_winner.py`) so the checkpoint pointer is never hand-copied.

    uv run python experiments/constitution-distill/scripts/write_matched_ood_config.py
"""
from __future__ import annotations

import json
from pathlib import Path

REPO_ROOT = Path(__file__).resolve().parents[3]
CKPT = REPO_ROOT / "experiments/constitution-distill/results-matched/ckpt_risk_averse_matched_prompts.json"
OUT = REPO_ROOT / "experiments/ood-evals/configs/config.eval-matched.yaml"

TEMPLATE = """\
# OOD suite, matched-prompts arm only — comparison rows for the merged run.
# Auto-generated by experiments/constitution-distill/scripts/write_matched_ood_config.py
# from results-matched/ckpt_risk_averse_matched_prompts.json (final teacher_kl {kl}).
#
#   uv run python experiments/ood-evals/flow.py --config configs/config.eval-matched.yaml --no-serve
#
# Single trained arm, pinned checkpoint (train_arm short-circuits to eval).
# 16384-token cap + thinking renderer, matching config.eval-highpower.yaml so
# the OOD rows are apples-to-apples with the high-power and 5-arm runs.
seed: 20260715

families:
  embedded_decision: 70
  agentic_tool: 70
  verbal_uncertainty: 64
  open_ended_allocation: 64
  calibration_threshold: 64

items_dir: items

student_model: Qwen/Qwen3-8B

arms:
  - name: risk_averse_matched_prompts   # constitution distill on SFT training-split prompts
    constitution: risk_averse
    checkpoint: {ckpt}

eval:
  concurrency: 48
  renderer: qwen3
  temperature: 0.6
  top_p: 0.95
  top_k: 20
  seed: 12345
  max_new_tokens: 16384
  limit_per_family: null

results:
  dir: experiments/ood-evals/results-matched
  gcs: <your-artifact-bucket>/ood-evals/
"""


def main() -> None:
    meta = json.loads(CKPT.read_text())
    ckpt = meta["checkpoint"]
    if not ckpt or not str(ckpt).startswith("tinker://"):
        raise SystemExit(f"unexpected checkpoint in {CKPT}: {ckpt!r}")
    OUT.write_text(TEMPLATE.format(ckpt=ckpt, kl=meta.get("final_teacher_kl")))
    print(f"[write_matched_ood_config] pinned {ckpt} -> {OUT.relative_to(REPO_ROOT)}")


if __name__ == "__main__":
    main()
